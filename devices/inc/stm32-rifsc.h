#ifndef stm32_rifsc_h
#define stm32_rifsc_h

#define HAVE_RIFSC

//STM32MP257
#if RIFSC_T_VERSION == 1

enum rifsc_cid_t
{
	CID_A35		= 1,
	CID_M33		= 2,
	CID_M0		= 3
};

enum rifsc_cidmask_t
{
	CID_MASK_A35	= (1 << CID_A35),
	CID_MASK_M33	= (1 << CID_M33),
	CID_MASK_M0		= (1 << CID_M0)
};

#else

#error Undefined or unspecified RIFSC_T_VERSION

#endif	//version check

#endif	//include guard
